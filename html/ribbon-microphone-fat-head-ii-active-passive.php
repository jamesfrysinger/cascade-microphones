					<h4>Fat Head II Active/Passive Details</h4>
					<p>The FAT HEAD II Active/Passive ribbon microphone is a first for this type of design. This unique feature allows the user to have the option to switch between either mode with a simple flick of an internal switch. The Active mode requires phantom power and will add up to 20db of gain, (this opens up the option to use this microphone with a wide range of preamps). The use of a very low noise differential amplifier circuit provides high gain while keeping the noise level low. We have optimized the circuit to preserve the warm, open, detailed tone of our Active/Passive ribbon mic by choosing operating conditions and electronic components that minimize tonal alterations. The active devices in the mic are factory matched for minimum distortion.</p>
					<p>By isolating the output of the ribbon from the microphone preamp the ribbon is consistently loaded at an optimal value resulting in less ribbon loading coloration. This set-up is extremely quiet and can handle 135 dB SPL. The Passive mode is best utilized with high gain preamps that are capable of powering the microphone with sufficient gain and eliminated the self noise that is usually evident in low gain preamps. Extremely long cable runs with minimal signal loss is possible due to the low-impedance output of the FAT HEAD II ACTIVE/PASSIVE, in addition, the ribbon element is resistant to damage by phantom power.</p>
					<p>&nbsp;</p>

					<h4>Technical Details</h4>
					<p>The active portion of the Fathead II Active/Passive ribbon microphone is a carefully adjusted differential cascade JFET amplifier where the phantom power supply 6K81 current limiting resistors are used as the cascode stage drain load resistors.</p>
					<p>The challenge of very low noise is met by using very high transconductance audio hand-matched JFETs operated at relatively low current.</p>
					<p>The operating point is adjusted to provide the best sound through a mixture of individual JFET source degeneration and differential operation. Stability with a wide range of audio cables is ensured by limiting the bandwidth of the differential cascode amplifier using two complementary frequency roll-off networks. These networks are realized with high quality polyphenylene sulfide chip capacitors and metal film resistors.</p>
					<p>The load for the ribbon was chosen by listening, as well, to open up the top end while providing good balance with the bass. In order to provide an appropriate signal level for the differential cascode amplifier the ribbon is interfaced through a 1:37 Lundahl LL2913 ribbon mic transformer.</p>
					<p>The switch provides either the transformer output signal directly or the further active amplified signal to the XLR connector on the mic.</p>
					<p>&nbsp;</p>
					
					<h4>Key Features</h4>
					<ul style="margin-left:18px;">
						<li>Phantom-powered Active electronics provide stable impedance and higher output for maximum compatibility with microphone preamplifiers</li>
						<li>Passive mode provides the option for use with a high-gain preamp (where extra gain is not needed)</li>
						<li>Smooth, warm and natural voicing for instrument reproduction High-SPL capability and extended frequency response</li>
						<li>Extremely powerful N50 neodymium magnets for high output level</li>
						<li>High sensitivity and low self-noise</li>
						<li>This microphone is perfect for use on a wide range of instruments (guitar cabs, vocals, strings, horns, drum overheads, orchestras and more) and live-stage settings.</li>
						<li>Active or Passive mode is activated by a simple flick-of-a-switch Active electronics are designed, manufactured and fitted in the USA</li>
						<li>Versatility, compatibility with the widest range of mic preamps, we have also made the active gain stage internally switchable, so that you can bypass the active gain stage, using a high quality switch with gold contacts.</li>
						<li>ACTIVE/PASSIVE switch is mounted internally for safety (caution should be used when engaging the Active switch, an additional 25db of gain will be added to the signal path)</li>
					</ul>
					
					

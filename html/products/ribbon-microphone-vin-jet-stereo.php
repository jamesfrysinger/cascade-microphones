					<p>The Cascade VIN-JET ribbon microphone features a 2 7/16” long ribbon of aluminum suspended in the field of a pair of Neodymium rare earth permanent magnets. When the ribbon is exposed to sound it vibrates within the magnetic field and a tiny current is induced in the ribbon.</p>

					<p>This current  is amplified to produce sound. What makes ribbon microphones so incredibly sensitive and accurate is that the signal comes directly from the material that senses the sound; nothing is lost in translation. The result is the most  intimate, warm, and articulate sound produced by any type of microphone. Ribbon microphones are an excellent choice for recording guitar cabinets, drum overheads and vocals.</p>

					<p>Neodymium rare earth permanent magnet  is a new kind of magnetic material developed in the 1980s with excellent magnetic characteristics in terms of a high-energy product, high coercive force etc. It is the strongest and most powerful magnetic material up to now and regarded as "The King of Magnets".</p>
